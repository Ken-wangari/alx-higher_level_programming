if __name__ == "__main__":
    """Print the sum, difference, product, and quotient of 10 and 5."""
    
    a = 10
    b = 5
    
    # Addition
    sum_result = a + b
    print("{} + {} = {}".format(a, b, sum_result))
    
    # Subtraction
    difference = a - b
    print("{} - {} = {}".format(a, b, difference))
    
    # Multiplication
    product = a * b
    print("{} * {} = {}".format(a, b, product))
    
    # Division (with float result)
    quotient = a / b
    print("{} / {} = {}".format(a, b, quotient))

